package day01.class05;

public class Test01 {
	public static void main(String[] args) {
		/*
		 * 변수
		 * - 값을 저장할 수 있는 메모리의 공간
		 * - 하나의 값을 담을 수 있다.
		 * - 변수를 사용하기 전에 선언을 먼저 해야 한다.
		 *   데이터타입 이름;
		 * - 변수 공간에 값을 대입하고 싶다면..
		 *   이름 = 값;  
		 * - 변수 공간을 만들면서 값을 대입한다.
		 *   데이터타입 이름 = 값;
		 */
		// 정수를 담는 i 란 이름의 변수 공간을 만들어라..
		int i;
		// i란 이름을 가지고 있는 변수에 1000을 넣어라..
		i = 1000;
		System.out.println(i);
		
		// 이름을 담을 수 있는 name이란 변수를 선언한다.
		String name; 
		
		// name 이란 변수에 나의이름을 넣는다.
		name = "손병찬";
		
		// name 이란 변수에 들어간 이름을 화면에 출력한다.
		System.out.println(name);
		
		int k = 100;
		
	}
}









