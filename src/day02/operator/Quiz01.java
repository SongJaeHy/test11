package day02.operator;

public class Quiz01 {
	public static void main(String[] args) {
		int a = 5;
		// 결과 예측하라..
		/*
		 * a = a + 1; // 6
		 * System.out.println(a)  // 6
		 */
		System.out.println(++a);
		/*
		 * System.out.println(a)  // 6
		 * a = a - 1; // 5
		 */
		System.out.println(a--);
		// 
		System.out.println(a);  // 5
		/*
		 * System.out.println(a)  // 5
		 * a = a + 1; // 6
		 */
		System.out.println(a++);
		/*
		 * a = a - 1; // 5
		 * System.out.println(a)  // 5
		 */
		System.out.println(--a);
	}
}
		
		
		
		
		
		
